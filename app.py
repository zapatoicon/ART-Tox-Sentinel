import streamlit as st
import pandas as pd
import os
import matplotlib.pyplot as plt
import seaborn as sns

st.set_page_config(
    page_title="ART-Tox Sentinel",
    page_icon="🏥",
    layout="wide"
)

# Load data
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
df = pd.read_csv(os.path.join(BASE_DIR, 'naija_art_clinic_clean.csv'))

# ── Derive combined risk flag ──────────────────────────────────────────────────
df['combined_risk_flag'] = (
    (df['renal_risk_flag'] == 1) & (df['cardio_risk_flag'] == 1)
).astype(int)

# ── Title & intro ──────────────────────────────────────────────────────────────
st.title("🏥 ART-Tox Sentinel")
st.markdown(
    "A prototype clinical decision-support dashboard simulating organ toxicity "
    "surveillance for HIV patients on first-line antiretroviral therapy (ART) in "
    "Nigerian clinic settings. Designed for use by clinic data officers and HIV "
    "program researchers evaluating regimen-level toxicity patterns."
)

# ── Synthetic data banner ──────────────────────────────────────────────────────
st.warning(
    "⚠️ **Simulation only.** All data in this dashboard is fully synthetic, "
    "generated to reflect Nigerian ART clinic parameters (WHO Nigeria 2025 guidelines, "
    "CKD-EPI renal equations). This tool is not validated for clinical use and should "
    "not inform real patient management decisions."
)

st.markdown("---")

# ── Workflow indicator ─────────────────────────────────────────────────────────
st.markdown("#### How This Dashboard Works")
st.markdown(
    "This tool follows a structured pipeline. Each stage feeds into the next:"
)
col_w1, col_w2, col_w3, col_w4 = st.columns(4)
with col_w1:
    st.info("**① INPUT**\n\nSynthetic patient records: demographics, ART regimen, routine lab values (creatinine, eGFR, BP, BMI, QTc)")
with col_w2:
    st.info("**② PROCESSING**\n\nRule-based risk logic applied to each patient record using WHO-aligned clinical thresholds")
with col_w3:
    st.info("**③ RISK OUTPUT**\n\nPatients flagged as: Renal Risk, CV Risk, Severe QTc Risk, or Combined Risk (renal + CV)")
with col_w4:
    st.info("**④ INTERPRETATION**\n\nRegimen-level summaries and monitoring recommendations generated from flagged cohort")

st.markdown("---")

# ── Risk logic transparency ────────────────────────────────────────────────────
st.markdown("#### Risk Flag Logic (Plain English)")
st.markdown("Every flag in this dashboard is generated by a simple, explainable rule. No black-box models.")

col_r1, col_r2, col_r3, col_r4 = st.columns(4)
with col_r1:
    st.error(
        "🔴 **Renal Risk**\n\n"
        "Creatinine **>1.3 mg/dL**\n\n"
        "*or* eGFR **<60 mL/min**\n\n"
        "*or* Urine protein **>30 mg/dL**\n\n"
        "→ Possible kidney stress from TDF accumulation"
    )
with col_r2:
    st.error(
        "🔴 **CV Risk**\n\n"
        "Systolic BP **>140 mmHg**\n\n"
        "*or* Diastolic BP **>90 mmHg**\n\n"
        "*or* Fasting glucose **100–125 mg/dL**\n\n"
        "*or* BMI **>30**\n\n"
        "→ Metabolic syndrome markers, linked to DTG weight gain"
    )
with col_r3:
    st.error(
        "🔴 **Severe QTc Risk**\n\n"
        "QTc **>450 ms** (male)\n\n"
        "QTc **>470 ms** (female)\n\n"
        "→ Cardiac conduction risk, linked to high BMI / metabolic syndrome"
    )
with col_r4:
    st.error(
        "🔴 **Combined Risk**\n\n"
        "Renal Risk flag **AND** CV Risk flag both active\n\n"
        "→ Highest-priority patients: dual organ stress requiring urgent regimen review"
    )

st.caption("Flags marked **−1** indicate missing lab data; risk status is indeterminate for those patients.")

st.markdown("---")

# ── Sidebar filters ────────────────────────────────────────────────────────────
st.sidebar.header("Filters")
regimen_filter = st.sidebar.multiselect(
    "Select ART Regimen:",
    options=df['art_regimen'].unique(),
    default=df['art_regimen'].unique(),
    placeholder="All selected"
)
sex_filter = st.sidebar.multiselect(
    "Select Sex:",
    options=df['sex'].unique(),
    default=df['sex'].unique(),
    placeholder="All selected"
)

df_filtered = df[
    (df['art_regimen'].isin(regimen_filter)) &
    (df['sex'].isin(sex_filter))
]

if df_filtered.empty:
    st.error("No patients match the selected filters. Adjust the sidebar selections.")
    st.stop()

# ── Key metrics ────────────────────────────────────────────────────────────────
st.subheader("Cohort Overview")
col1, col2, col3, col4, col5 = st.columns(5)
with col1:
    st.metric("Patients in View", len(df_filtered))
with col2:
    renal_pct = (df_filtered['renal_risk_flag'] == 1).mean() * 100
    st.metric("Renal Risk", f"{renal_pct:.1f}%",
              help="Creatinine >1.3 mg/dL, eGFR <60 mL/min, or proteinuria >30 mg/dL")
with col3:
    cv_pct = (df_filtered['cardio_risk_flag'] == 1).mean() * 100
    st.metric("CV Risk", f"{cv_pct:.1f}%",
              help="SBP >140 mmHg, DBP >90 mmHg, fasting glucose 100–125 mg/dL, or BMI >30")
with col4:
    qtc_pct = (df_filtered['severe_qtc_flag'] == 1).mean() * 100
    st.metric("Severe QTc Risk", f"{qtc_pct:.1f}%",
              help="QTc >450 ms (male) or >470 ms (female)")
with col5:
    combined_pct = (df_filtered['combined_risk_flag'] == 1).mean() * 100
    st.metric("Combined Risk", f"{combined_pct:.1f}%",
              help="Patients flagged for both renal AND cardiovascular risk simultaneously")

st.markdown("---")

