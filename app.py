with st.expander("About This Tool / Methods"):
    st.markdown("""
### What is ART-Tox Sentinel?

ART-Tox Sentinel is a prototype health informatics dashboard built to simulate
automated toxicity surveillance for HIV patients on first-line antiretroviral therapy
in Nigerian public sector clinic settings. It demonstrates how routine laboratory
data such as renal markers, cardiometabolic indicators and ECG-derived QTc intervals 
can be structured into a risk-flagging pipeline to support clinical monitoring workflows.

---

### Data

All patient data used in this dashboard is **fully synthetic**. It was generated
programmatically to reflect realistic Nigerian ART clinic demographics and clinical
distributions, based on the following sources:

- WHO Nigeria ART guidelines (2025 update)
- Nigerian HIV/AIDS Indicator and Impact Survey (NAIIS) demographic proportions
- Published Nigerian cohort data on ART-associated nephrotoxicity and weight gain

The synthetic cohort (n = 500) includes mechanism-based missing data to simulate
rural clinic data quality gaps, including missing BP readings, creatinine values,
and ECG results.

---

### Risk Flag Definitions

| Flag | Threshold |
|------|-----------|
| Renal Risk | Creatinine >1.3 mg/dL, OR eGFR <60 mL/min, OR urine protein >30 mg/dL |
| CV Risk | SBP >140 mmHg, OR DBP >90 mmHg, OR fasting glucose 100–125 mg/dL, OR BMI >30 |
| Severe QTc | QTc >450 ms (male), QTc >470 ms (female) |
| Combined Risk | Renal Risk AND CV Risk both flagged simultaneously |
| Unknown (−1) | Relevant lab values missing; risk status indeterminate |

eGFR calculated using the **CKD-EPI 2009 equation** (race-free formulation),
incorporating age, sex, and serum creatinine.

---

### Limitations

- All findings are derived from synthetic data and carry no clinical validity
- Risk thresholds are rule-based; no machine learning or statistical modelling is applied
- The tool has not been tested in or validated against any real clinical environment
- Missing data handling is limited to high-risk patient imputation only
- This prototype does not account for drug duration, adherence, or comorbidity burden

---

### Intended Use

This tool is intended as a **proof-of-concept** for researchers, health informaticians,
and digital health practitioners interested in ART toxicity monitoring infrastructure
for low-resource settings. It is not suitable for clinical deployment in its current form.

---

*Built with Python, Streamlit, pandas, matplotlib, and seaborn.*
*ART-Tox Sentinel v0.1 — Open source prototype.*
    """)

